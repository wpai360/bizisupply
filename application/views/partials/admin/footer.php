      $.ajax({
        url: "<?php echo base_url('admin/new-notifications');?>",
        type: 'POST',
        dataType: 'JSON', 
        success: function(data) {
        //console.log(data);
        jQuery('#msg-notif').html(data[0]);
        jQuery('#total').html('You have <b>'+data[1]+'</b> notifications');
        if(data[2]==1){
          jQuery('#newnoti').text(data[2]);
          jQuery('#newnoti').show();
        }else{
          jQuery('#newnoti').hide();
        }
      }              
    });
  }
</script>
